const filters = ["All", "Websites", "Flayers", "Business Cards"];

export default class Portfolio extends Component {
  state = { selected: "All", projects: dataBase };

  selectFilter = (filterT) => {
    const selectedFilter = filterT.target.textContent;
    let filtredDataBase = dataBase;
    if (selectedFilter !== "All") {
      filtredDataBase = dataBase.filter((e) => e.category === selectedFilter);
    }
    this.setState({
      selected: selectedFilter,
      projects: filtredDataBase,
    });
  };

  render() {
    return (
      <>
        <Tolbar
          filters={filters}
          selected={this.state.selected}
          onSelectFilter={this.selectFilter}
        />
        <ProjectList projects={this.state.projects} />
      </>
    );
  }
}
